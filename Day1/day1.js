//solution 2
function countChar(str, char) {
  let count = 0;
  for (let i = 0; i < str.length; i++) {
    if (str.charAt(i) === char) {
      count++;
    }
  }
  return count;
}
let upCount = countChar(directions, "<");
let downCount = countChar(directions, ">");

console.log(upCount - downCount);
